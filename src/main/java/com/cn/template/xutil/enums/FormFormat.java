package com.cn.template.xutil.enums;

/**
 * 表单的格式.
 * 
 * @author Libra
 *
 */
public enum FormFormat {
	/** 中文 */
	CHINESE("中文", 0),
	/** 双语(中英) */
	DOUBLE("双语(中英)",1);

	private String value;
	private int index;

	/**
	 * 构造赋值.
	 * 
	 * @param value
	 * @param index
	 */
	private FormFormat(String value, int index) {
		this.value = value;
		this.index = index;
	}

	/**
	 * 取得枚举量的名称.
	 * 
	 * @param index
	 * @return
	 */
	public static String getName(int index) {
		for (FormFormat c : FormFormat.values()) {
			if (c.getIndex() == index) {
				return c.value;
			}
		}
		return null;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}
}
